ALTER TABLE "audit_logs"
  ADD COLUMN "visibility" character varying NOT NULL DEFAULT 'visible';

CREATE INDEX IF NOT EXISTS "auditlog_visibility_created_at"
  ON "audit_logs" ("visibility", "created_at" DESC, "id" DESC);

CREATE TABLE IF NOT EXISTS "audit_visibility_defaults" (
  "key" character varying NOT NULL,
  "strategy" character varying NOT NULL DEFAULT 'visible',
  "updated_at" timestamptz NOT NULL DEFAULT NOW(),
  "updated_by" bigint NULL,
  "updated_by_name" character varying NOT NULL DEFAULT '',
  PRIMARY KEY ("key")
);

CREATE TABLE IF NOT EXISTS "audit_visibility_overrides" (
  "id" bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  "source" character varying NOT NULL,
  "action_key" character varying NOT NULL,
  "strategy" character varying NOT NULL DEFAULT 'visible',
  "description" character varying NOT NULL DEFAULT '',
  "created_at" timestamptz NOT NULL DEFAULT NOW(),
  "created_by" bigint NULL,
  "created_by_name" character varying NOT NULL DEFAULT '',
  "updated_at" timestamptz NOT NULL DEFAULT NOW(),
  "updated_by" bigint NULL,
  "updated_by_name" character varying NOT NULL DEFAULT '',
  PRIMARY KEY ("id")
);

CREATE UNIQUE INDEX IF NOT EXISTS "audit_visibility_overrides_source_action_key"
  ON "audit_visibility_overrides" ("source", "action_key");
CREATE INDEX IF NOT EXISTS "audit_visibility_overrides_strategy"
  ON "audit_visibility_overrides" ("strategy");

