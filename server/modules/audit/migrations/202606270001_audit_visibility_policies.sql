COMMENT ON COLUMN "audit_visibility_defaults"."updated_by" IS '最近更新默认策略的用户 ID，为空表示系统写入';
COMMENT ON COLUMN "audit_visibility_defaults"."updated_by_name" IS '最近更新默认策略的用户名或显示名快照';

