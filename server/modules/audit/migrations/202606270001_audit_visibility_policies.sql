COMMENT ON COLUMN "audit_visibility_overrides"."description" IS '覆盖策略的补充说明';
COMMENT ON COLUMN "audit_visibility_overrides"."created_at" IS '覆盖策略创建时间';
COMMENT ON COLUMN "audit_visibility_overrides"."created_by" IS '创建覆盖策略的用户 ID，为空表示系统写入';
