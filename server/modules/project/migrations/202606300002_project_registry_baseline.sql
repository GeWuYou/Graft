CREATE TABLE IF NOT EXISTS compose_projects (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  display_name character varying NOT NULL,
  canonical_project_name character varying NOT NULL,
  canonical_project_name_source character varying NOT NULL,
  source_kind character varying NOT NULL,
  host_scope character varying NOT NULL,
  working_directory text NOT NULL,
  ownership_mode character varying NOT NULL,
  last_refresh_status character varying NOT NULL DEFAULT 'never',
  last_refresh_at timestamptz NULL,
  last_refresh_error_code character varying NOT NULL DEFAULT '',
  last_refresh_error_message text NOT NULL DEFAULT '',
  last_refresh_config_hash character varying NOT NULL DEFAULT '',
  last_observed_config_hash character varying NOT NULL DEFAULT '',
  last_drift_checked_at timestamptz NULL,
  drift_status character varying NOT NULL DEFAULT 'unknown',
  created_by bigint NULL,
  updated_by bigint NULL,
  deleted_by bigint NULL,
  created_at timestamptz NOT NULL DEFAULT NOW(),
  updated_at timestamptz NOT NULL DEFAULT NOW(),
  deleted_at bigint NOT NULL DEFAULT 0,
  PRIMARY KEY (id),
  CONSTRAINT compose_projects_canonical_name_source_check CHECK (canonical_project_name_source IN ('computed', 'override')),
  CONSTRAINT compose_projects_source_kind_check CHECK (source_kind IN ('imported', 'managed', 'git', 'template')),
  CONSTRAINT compose_projects_host_scope_check CHECK (host_scope IN ('local')),
  CONSTRAINT compose_projects_ownership_mode_check CHECK (ownership_mode IN ('external', 'managed-root-dedicated')),
  CONSTRAINT compose_projects_last_refresh_status_check CHECK (last_refresh_status IN ('never', 'success', 'failed')),
  CONSTRAINT compose_projects_drift_status_check CHECK (drift_status IN ('unknown', 'clean', 'changed', 'missing'))
);

CREATE UNIQUE INDEX IF NOT EXISTS compose_projects_host_scope_canonical_project_name_live
  ON compose_projects (host_scope, canonical_project_name)
  WHERE deleted_at = 0;
CREATE UNIQUE INDEX IF NOT EXISTS compose_projects_working_directory_live
  ON compose_projects (working_directory)
  WHERE deleted_at = 0;
CREATE INDEX IF NOT EXISTS compose_projects_refresh_status_updated
  ON compose_projects (deleted_at, last_refresh_status, updated_at DESC, id DESC);
CREATE INDEX IF NOT EXISTS compose_projects_drift_status_updated
  ON compose_projects (deleted_at, drift_status, updated_at DESC, id DESC);

CREATE TABLE IF NOT EXISTS compose_project_files (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  project_id bigint NOT NULL,
  kind character varying NOT NULL,
  role character varying NOT NULL,
  absolute_path text NOT NULL,
  display_path text NOT NULL,
  order_index integer NOT NULL,
  exists_on_last_refresh boolean NOT NULL DEFAULT true,
  last_observed_hash character varying NOT NULL DEFAULT '',
  created_at timestamptz NOT NULL DEFAULT NOW(),
  updated_at timestamptz NOT NULL DEFAULT NOW(),
  PRIMARY KEY (id),
  CONSTRAINT compose_project_files_project_id_fkey
    FOREIGN KEY (project_id) REFERENCES compose_projects (id) ON DELETE CASCADE,
  CONSTRAINT compose_project_files_kind_check CHECK (kind IN ('compose', 'env')),
  CONSTRAINT compose_project_files_role_check CHECK (role IN ('primary', 'override', 'env'))
);

CREATE UNIQUE INDEX IF NOT EXISTS compose_project_files_project_order
  ON compose_project_files (project_id, order_index);
CREATE UNIQUE INDEX IF NOT EXISTS compose_project_files_project_absolute_path
  ON compose_project_files (project_id, absolute_path);
CREATE INDEX IF NOT EXISTS compose_project_files_project_kind_role
  ON compose_project_files (project_id, kind, role, order_index);

CREATE TABLE IF NOT EXISTS compose_project_snapshots (
  project_id bigint NOT NULL,
  normalized_compose_json jsonb NOT NULL,
  config_hash character varying NOT NULL,
  declared_service_count integer NOT NULL DEFAULT 0,
  declared_services_digest character varying NOT NULL DEFAULT '',
  refreshed_at timestamptz NOT NULL,
  PRIMARY KEY (project_id),
  CONSTRAINT compose_project_snapshots_project_id_fkey
    FOREIGN KEY (project_id) REFERENCES compose_projects (id) ON DELETE CASCADE
);

